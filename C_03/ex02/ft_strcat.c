char	*ft_strcat(char *dest, char *src)
{
	int	i1;
	int	i2;

	i1 = 0;
	i2 = 0;
	if (!src)
		return (0);
	while (dest[i2])
	{
		i2++;
	}
	while (src[i1])
	{
		dest[i2] = src[i1];
		i1++;
		i2++;
	}
	dest[i2] = '\0';
	return (dest);
}
